char* LCD_Itoa(char *buf, long i, int len, char dec)
{
	char  *s;
	long  rem;
	long  tmp;
	char  index = 0;

	tmp = i;
	buf[len] = 0;
	s = &buf[len];               

	if (tmp<0)
	{
		tmp = - tmp;
	}
	while( len-- )
	{
		if( dec != 0 )             
		{
			index++;                
			if(index == dec + 1)    
			{
				*--s = '.';
			}
			else                    
			{
				rem = tmp % 10;
				*--s = rem + '0'; 
				tmp /= 10;
			}
		}
		else
		{
			rem = tmp % 10;
			*--s = rem + '0';
			tmp /= 10;
		}
	}

	if (i < 0)
	{
		buf[0]='-';
	}
	return(buf);
}

